package com.tekparams.training_pragna.erp;

public class For_Loop_Pramid_Example {
    public static void main(String[] args)
    {
        int i;
        int j;
        for (i=1;i<=5;i++)
        {
            for(j=1;j<i;j++)
            {
                System.out.println("*");
            }
        }
    }
}
