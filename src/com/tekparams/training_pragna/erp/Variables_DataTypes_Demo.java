package com.tekparams.training_pragna.erp;

public class Variables_DataTypes_Demo {

    public static void main(String[] args)
    {
        byte age;
        age=30;
        System.out.println("age : "+age);
        Byte.toString(age);


        short amount;
        amount=5000;
        System.out.println("amount : "+amount);

        int amount1;
        amount1=6546;
        System.out.println("int amount : "+amount1);

        float fAmount;
        fAmount=5000.50f;
        System.out.println("Amount in decimals : "+fAmount);

        double dAmount;
        dAmount=1142333.67;
        System.out.println("Amount in decimals (declared as double) "+dAmount);

        char option;
        option = '%';
        System.out.println("declared as character : "+option);

        String firstName;
        firstName="Suresh Karthik";
        System.out.println("First Name : "+firstName);

        boolean isAmountZero;
        isAmountZero=true;
        System.out.println("Is Amount equal to zero : "+isAmountZero);






    }
}
